"""CSV and TeX reporting helpers for preservation outputs."""

from __future__ import annotations

import csv
from pathlib import Path
from typing import Any, Mapping

from lib.chain_families import CHAIN_FAMILY_DISPLAY_NAMES, CHAIN_FAMILY_MACRO_NAMES


def write_csv_with_headers(
    path: Path,
    fieldnames: list[str],
    rows: list[Mapping[str, Any]],
) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=fieldnames)
        writer.writeheader()
        for row in rows:
            writer.writerow({field: row.get(field, "") for field in fieldnames})


def _safe_float(value: Any) -> float | None:
    try:
        if value == "":
            return None
        return float(value)
    except (TypeError, ValueError):
        return None


def _format_int(value: int) -> str:
    return f"{int(value):,}"


def _format_float(value: Any, digits: int = 3, fallback: str = "0.000") -> str:
    parsed = _safe_float(value)
    if parsed is None:
        return fallback
    return f"{parsed:.{digits}f}"


def write_preservation_tex(path: Path, summary: Mapping[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    coverage = dict(summary.get("coverage", {}))
    overall = dict(summary.get("overall", {}))
    by_family = {
        str(row.get("chain_family", "")): dict(row)
        for row in summary.get("by_family", [])
    }
    lines = [
        "% Auto-generated by scripts/eval/export_preservation_stats.py",
        f"\\providecommand{{\\ChainPreservationRows}}{{{_format_int(int(coverage.get('rows', 0)))}}}",
        f"\\providecommand{{\\ChainPreservationOkRows}}{{{_format_int(int(coverage.get('status_ok', 0)))}}}",
        f"\\providecommand{{\\ChainPreservationOkFraction}}{{{_format_float(coverage.get('ok_fraction'), 3, '0.000')}}}",
        f"\\providecommand{{\\ChainPreservationAsrRows}}{{{_format_int(int(coverage.get('asr_rows', 0)))}}}",
        f"\\providecommand{{\\ChainPreservationSpeakerRows}}{{{_format_int(int(coverage.get('speaker_rows', 0)))}}}",
        f"\\providecommand{{\\ChainPreservationOverallWERDrift}}{{{_format_float(overall.get('wer_drift_mean'))}}}",
        f"\\providecommand{{\\ChainPreservationOverallCERDrift}}{{{_format_float(overall.get('cer_drift_mean'))}}}",
        f"\\providecommand{{\\ChainPreservationOverallSpeakerCosine}}{{{_format_float(overall.get('speaker_similarity_mean'))}}}",
        f"\\providecommand{{\\ChainPreservationOverallDurationRatio}}{{{_format_float(overall.get('duration_ratio_to_parent_mean'))}}}",
        f"\\providecommand{{\\ChainPreservationOverallAbsRMSDrift}}{{{_format_float(overall.get('rms_dbfs_delta_abs_mean'))}}}",
    ]
    for family, macro_suffix in CHAIN_FAMILY_MACRO_NAMES.items():
        family_row = by_family.get(family, {})
        lines.extend(
            [
                f"\\providecommand{{\\ChainPreservation{macro_suffix}WERDrift}}{{{_format_float(family_row.get('wer_drift_mean'))}}}",
                f"\\providecommand{{\\ChainPreservation{macro_suffix}CERDrift}}{{{_format_float(family_row.get('cer_drift_mean'))}}}",
                f"\\providecommand{{\\ChainPreservation{macro_suffix}SpeakerCosine}}{{{_format_float(family_row.get('speaker_similarity_mean'))}}}",
                f"\\providecommand{{\\ChainPreservation{macro_suffix}DurationRatio}}{{{_format_float(family_row.get('duration_ratio_to_parent_mean'))}}}",
                f"\\providecommand{{\\ChainPreservation{macro_suffix}AbsRMSDrift}}{{{_format_float(family_row.get('rms_dbfs_delta_abs_mean'))}}}",
            ]
        )
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def write_preservation_table_tex(path: Path, summary: Mapping[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    rows = list(summary.get("by_family", []))
    lines = [
        "% Auto-generated by scripts/eval/export_preservation_stats.py",
        "\\begin{table}[t]",
        "\\centering",
        "\\footnotesize",
        "\\setlength{\\tabcolsep}{4pt}",
        "\\caption{Counterfactual preservation validation by delivery family. $\\Delta$WER and $\\Delta$CER denote child minus clean-parent error rates against the reference transcript; lower is better. Speaker cosine is higher-is-better. Duration ratio and $|\\Delta\\mathrm{RMS}|$ summarize lightweight acoustic drift.}",
        "\\label{tab:exp_preservation}",
        "\\begin{tabular}{lccccc}",
        "\\toprule",
        "Family & $\\Delta$WER & $\\Delta$CER & Speaker cos. & Dur. ratio & $|\\Delta\\mathrm{RMS}|$ \\\\",
        "\\midrule",
    ]
    for row in rows:
        family = str(row.get("chain_family", ""))
        lines.append(
            f"{CHAIN_FAMILY_DISPLAY_NAMES.get(family, family)}"
            f" & {_format_float(row.get('wer_drift_mean'), fallback='--')}"
            f" & {_format_float(row.get('cer_drift_mean'), fallback='--')}"
            f" & {_format_float(row.get('speaker_similarity_mean'), fallback='--')}"
            f" & {_format_float(row.get('duration_ratio_to_parent_mean'), fallback='--')}"
            f" & {_format_float(row.get('rms_dbfs_delta_abs_mean'), fallback='--')} \\\\"
        )
    lines.extend(
        [
            "\\bottomrule",
            "\\end{tabular}",
            "\\end{table}",
        ]
    )
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


__all__ = [
    "write_csv_with_headers",
    "write_preservation_table_tex",
    "write_preservation_tex",
]
